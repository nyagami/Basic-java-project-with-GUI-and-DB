    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int rows = contentTable.getRowCount();
        for(int i=0;i<rows;i++){
            int ma = Integer.parseInt(contentTable.getValueAt(i, 0).toString());
            String ngayGiaoDich = contentTable.getValueAt(i, 1).toString();
            String maSP = contentTable.getValueAt(i, 2).toString();
            String maKH = contentTable.getValueAt(i, 3).toString();
            GiaoDichDB.update(new GiaoDich(ma, ngayGiaoDich, maSP, maKH));
        }
        refresh();
    }//GEN-LAST:event_jButton2ActionPerformed

