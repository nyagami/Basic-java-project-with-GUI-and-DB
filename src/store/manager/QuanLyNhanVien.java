    // n√∫t l∆∞u
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int rows = contentTable.getRowCount();
        for(int i=0;i<rows;i++){
            String ma = contentTable.getValueAt(i, 0).toString();
            String hoTen = contentTable.getValueAt(i, 1).toString();
            String diaChi = contentTable.getValueAt(i, 2).toString();
            String ngaySinh = contentTable.getValueAt(i, 3).toString();
            String gioiTinh = contentTable.getValueAt(i, 4).toString();
            String sdt = contentTable.getValueAt(i, 5).toString();
            NhanvienDB.update(new NhanVien(ma, hoTen, diaChi, ngaySinh, gioiTinh, sdt));
        }
        refresh();
    }//GEN-LAST:event_jButton2ActionPerformed

    // n√∫t th√™m
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String maNV = maInput.getText();
        String hoTen = hoTenInput.getText();
        if(maNV == null || hoTen == null || maNV.length() == 0 || hoTen.length() == 0){
            inputError.setVisible(true);
            return;
        }
        String diaChi = diaChiInput.getText();
        String ngaySinh = ngaySinhInput.getText();
        String gioiTinh = gioiTinhInput.getText();
        String sdt = sdtInput.getText();
        NhanvienDB.insert(new NhanVien(maNV, hoTen, diaChi, ngaySinh, gioiTinh, sdt));
        this.refresh();
    }//GEN-LAST:event_jButton4ActionPerformed

