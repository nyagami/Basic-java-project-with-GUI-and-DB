    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // n√∫t l∆∞u
        String maSP = maInput.getText();
        String ten = tenInput.getText();
        if(maSP == null || ten == null || maSP.length() == 0 || ten.length() == 0){
            inputError.setVisible(true);
            return;
        }
        String hang = hangInput.getText();
        String phienBan = phienBanInput.getText();
        String trangThai = trangThaiInput.getText();
        int soLuong = Integer.parseInt(soLuongInput.getText().isBlank() ? "0" : soLuongInput.getText());
        SanPhamDB.insert(new SanPham(maSP, ten, hang, phienBan, trangThai, soLuong));
        this.refresh();
    }//GEN-LAST:event_jButton4ActionPerformed

