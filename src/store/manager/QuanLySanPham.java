    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int rows = contentTable.getRowCount();
        for(int i=0;i<rows;i++){
            String ma = contentTable.getValueAt(i, 0).toString();
            String ten = contentTable.getValueAt(i, 1).toString();
            String hang = contentTable.getValueAt(i, 2).toString();
            String phienBan = contentTable.getValueAt(i, 3).toString();
            String trangThai = contentTable.getValueAt(i, 4).toString();
            int soLuong = Integer.parseInt(
                    contentTable.getValueAt(i, 5).toString().isBlank() ? "0" : contentTable.getValueAt(i, 5).toString()
            );
            SanPhamDB.update(new SanPham(ma, ten, hang, phienBan, trangThai, soLuong));
        }
        refresh();
    }//GEN-LAST:event_jButton2ActionPerformed

